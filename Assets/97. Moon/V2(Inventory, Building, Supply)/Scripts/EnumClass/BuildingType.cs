public enum BuildingType
{
    Inn, // (여관(숙소): 스트레스 해소)
    Tavern, // (식당: 체력 회복)
    Smithy, // (대장간: 장비 강화(개척 데이터와 재료 소모))
    Storage, // (창고: 탐사 종료 후 보급품을 보관할 수 있는 양을 늘려주는 창고. 업글시 슬롯 증가 + 창고에 쌓을 수 있는 아이템 개수 증가(ex: 8개 -> 16개))
    Guild, // (탐사 데이터 추가 수급처)
    Carriage, // 탐사(이동) 인벤토리 확장용 (신규)
    BuildinnA
}